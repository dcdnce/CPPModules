#include "Dog.hpp"

Dog::Dog(void) {
	std::cout << "Dog Default constructor called" << std::endl;
	this->type = "Dog";
	this->_brain = new Brain();
}

Dog::Dog(const Dog& src){
	std::cout << "Dog Copy constructor called" << std::endl;
	this->_brain = new Brain();
	*this = src;
}

Dog::~Dog(void) {
	std::cout << "Dog Default destructor called" << std::endl;
	delete this->_brain;
}

